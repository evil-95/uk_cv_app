    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String Email = jTextField1.getText();
        String nom = jTextField2.getText();
        String prénom = jTextField3.getText();
        String password = jPasswordField1.getText();

           
            // Préparation de la requête de mise a jour 
            try {
                // Préparer la requête de suppression avec un paramètre
                Connect();
                String requetemodification;
                requetemodification = "UPDATE  inscription SET nom = ?, prénom = ?, password = ?, Confirm_Password = ? WHERE Email = ?";
                pst = con4.prepareStatement(requetemodification);
                pst.setString(1, nom);
                pst.setString(2, prénom);
                pst.setString(3, password);
                pst.setString(4, password);
                pst.setString(5, Email);
                // execution de la requette de mise a jour
                int lignesModifiees = pst.executeUpdate();
                if (lignesModifiees > 0) {
                    System.out.println("L'utilisateur a été modifié avec succès !");
                    JOptionPane.showMessageDialog(null,"utilisateur modifié avec succes");
                } else {
                    System.out.println("Aucune correspondance trouvée pour l'email spécifié. Aucune modification effectuée.");
                    JOptionPane.showMessageDialog(null,"Aucune correspondance trouvée pour l'email spécifié. Aucune modification effectuée.");
                }
                // Fermer la connexion
                con4.close();
                Table();
               // pst.close();
            }catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "le probleme est la "+e);
            
        }


    }//GEN-LAST:event_jButton1ActionPerformed

