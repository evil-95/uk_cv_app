       public void Table(){
        String[] Inscription = {"Email","Nom","Prenom","password"};
        String[] afficher = new String[5];
        DefaultTableModel model = new DefaultTableModel(null,Inscription);
        
        try{
            Connect();
            Statement st = (Statement) con4.createStatement();
            rs = st.executeQuery("SELECT * FROM Inscription");
            
            while (rs.next()){
                afficher[0] = rs.getString("Email");
                afficher[1] = rs.getString("Nom");
                afficher[2] = rs.getString("Prénom");
                afficher[3] = rs.getString("password");
                model.addRow(afficher);
            }
            jTable1.setModel(model);
            con4.close();
        }catch(Exception e){ 
            e.printStackTrace();
            
        }
    }
        

